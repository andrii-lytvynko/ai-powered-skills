function NavHeader({ onProductChange, selectedProduct, products = [], isCollapsed }) {
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef(null);

  useEffect(() => {
    function handleClickOutside(event) {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setIsOpen(false);
      }
    }

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside);
      return () => document.removeEventListener('mousedown', handleClickOutside);
    }
  }, [isOpen]);

  const handleProductSelect = (product) => {
    if (onProductChange) {
      onProductChange(product);
    }
    setIsOpen(false);
  };

  return (
    <div className={`ai-agents-nav-header ${isCollapsed ? 'ai-agents-nav-header--collapsed' : ''}`} ref={dropdownRef}>
      <div className="ai-agents-nav-header__logo">
        <AIAgentsLogoIcon className="ai-agents-nav-header__logo-icon" />
      </div>
      {!isCollapsed && (
        <>
          <button 
            className={`ai-agents-nav-header__product-selector ${isOpen ? 'active' : ''}`}
            onClick={() => setIsOpen(!isOpen)}
          >
            <span className="ai-agents-nav-header__product-name">{selectedProduct?.name || 'AI agents'}</span>
            <ChevronDownIcon className={`ai-agents-nav-header__chevron ${isOpen ? 'rotated' : ''}`} />
          </button>
          
          {isOpen && products.length > 0 && (
            <div className="ai-agents-nav-header__dropdown">
              <div className="ai-agents-nav-header__dropdown-list">
                {products.map((product) => {
                  const Icon = product.icon;
                  const isSelected = selectedProduct?.id === product.id;
                  return (
                    <button
                      key={product.id}
                      className={`ai-agents-nav-header__dropdown-item ${isSelected ? 'selected' : ''}`}
                      onClick={() => handleProductSelect(product)}
                    >
                      <Icon className="ai-agents-nav-header__dropdown-icon" />
                      <span className="ai-agents-nav-header__dropdown-name">{product.name}</span>
                      {isSelected && <CheckIcon className="ai-agents-nav-header__dropdown-check" />}
                    </button>
                  );
                })}
              </div>
            </div>
          )}
        </>
      )}
    </div>
  );
}


function FilterDropdown({ label, value, options, onChange }) {
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef(null);

  useEffect(() => {
    function handleClickOutside(event) {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setIsOpen(false);
      }
    }

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside);
      return () => document.removeEventListener('mousedown', handleClickOutside);
    }
  }, [isOpen]);

  return (
    <div className="ai-agents-filter" ref={dropdownRef}>
      <label className="ai-agents-filter__label">{label}</label>
      <button 
        className={`ai-agents-filter__select ${isOpen ? 'active' : ''}`}
        onClick={() => setIsOpen(!isOpen)}
      >
        <span className="ai-agents-filter__value">{value}</span>
        <ChevronDownIcon className={`ai-agents-filter__chevron ${isOpen ? 'rotated' : ''}`} />
      </button>
      
      {isOpen && (
        <div className="ai-agents-filter__dropdown">
          {options.map((option, index) => (
            <button
              key={index}
              className={`ai-agents-filter__option ${option === value ? 'selected' : ''}`}
              onClick={() => {
                onChange(option);
                setIsOpen(false);
              }}
            >
              {option}
              {option === value && <CheckIcon className="ai-agents-filter__check" />}
            </button>
          ))}
        </div>
      )}
    </div>
  );
}

